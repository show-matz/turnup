#include "Utilities.hxx"

using namespace std;

namespace turnup {

    //--------------------------------------------------------------------------
    //
    // implementation of class Utilities
    //
    //--------------------------------------------------------------------------
    void Utilities::Trim( const char*& p1, const char*& p2 ) {
        for( ; p1 < p2; ++p1 ) {
            if( *p1 != ' ' && *p1 != 0x09 )
                break;
        }
        for( ; p1 < p2; --p2 ) {
            if( p2[-1] != ' ' && p2[-1] != 0x09 )
                break;
        }
    }

}

